// new for 2022

// .at()

// useful for arrays and strings

const arr = [100, 10, 50, 50000, 10000]; 

// get second last item
arr[arr.length -2]      // 50000

// with at()

arr.at(-2); //  50000 (little bit easier to read)

const aString = 'the quick brown fox jumped over the lazy dog';

aString.at(-10) // 'e'

